void drawChar8x8(uint16_t x, uint16_t y, char c, uint16_t color, uint8_t inversion) {
    unsigned char uc = (unsigned char)c;
    if (uc < 32 || uc > 127) {
        // Рисуем placeholder для неподдерживаемых символов
        for (int i = 0; i < 8; i++)
            for (int j = 0; j < 8; j++)
                ILI9225_drawPixel(x + i, y + j, color);
        return;
    }
    for (uint8_t col = 0; col < 8; col++) {
        uint8_t bits = Font8x8[uc][col];
        if (inversion == 1){
                ILI9225_drawPixel(x + col, y + 8, color);
        }
        for (uint8_t row = 0; row < 8; row++) {
            if (inversion == 1){
                if (!(bits & (1 << (7 - row)))) {
                    ILI9225_drawPixel(x + col, y + row, color);
                }
            }
            else {
                if (bits & (1 << (7 - row))) {
                    ILI9225_drawPixel(x + col, y + row, color);
                }
            }
        }
    }
}

void drawString8x8(uint16_t x, uint16_t y, const char *str, uint16_t color, uint8_t inversion) {
    uint16_t cursor_x = x;
    while (*str) {
        drawChar8x8(cursor_x, y, *str, color, inversion);
        cursor_x += 8;
        str++;
    }
}